<?php
// Tipe Data String
$judul_buku = "Dasar-Dasar Web";
var_dump($judul_buku); // Output: string(15) "Dasar-Dasar Web"

// Tipe Data Integer
$harga = 95000;
var_dump($harga); // Output: int(95000)

// Tipe Data Float (Double)
$rating = 4.5;
var_dump($rating); // Output: float(4.5)

// Tipe Data Boolean
$tersedia = true;
var_dump($tersedia); // Output: bool(true)

// Tipe Data Array
$kategori = array("Pemrograman", "Desain", "Jaringan");
var_dump($kategori); // Output: array(3) { [0]=> string(11) "Pemrograman" ... }

// Tipe Data NULL
$diskon = null;
var_dump($diskon); // Output: NULL
?>